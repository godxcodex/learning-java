package operators;

public class addandequalto {
    public static void main(String[] args)
    {
        int a = 2;
        int b = 3;
        b *= 5;
        System.out.println(b);
        b += 4;
        System.out.println(b);
        System.out.println(a>2);//comparison operator
        System.out.println(a<b && a<b);//and operator
        System.out.println(9<50 || 20<10);// OR operator
        System.out.println(2 != 1);


    }
}
